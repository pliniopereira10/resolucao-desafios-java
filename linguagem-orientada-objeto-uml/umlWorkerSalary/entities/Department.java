//	Creation of the class to identify the employee's department name  

package umlWorkerSalary.entities;

public class Department {

	private String name;
	
	public Department() {
		
	}

	public Department(String name) {
		this.name = name;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
}
